    public void mostrarMensajeEntrada(int codigoError, String idEmpleado, String fecha, String hora) {
        switch (codigoError) {
            case 0 -> {
                Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
                JOptionPane.showMessageDialog(this, 
                        "No existe un empleado con ID " + idEmpleado, "Error", JOptionPane.ERROR_MESSAGE);
            }
            case 1 -> {
                Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
                JOptionPane.showMessageDialog(this, 
                        "El empleado ID " + idEmpleado + " no está activo en la planta", 
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
            case 2 -> {
                Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
                JOptionPane.showMessageDialog(this, 
                        "No puede marcar la entrada. No debe trabajar los " + 
                         Conversion.obtenerDiaSemana(fecha), 
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
            case 3 -> {
                Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
                JOptionPane.showMessageDialog(this, 
                        "No puede marcar la entrada. No debe trabajar por feriado no laborable en la fecha " + fecha, 
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
            case 4 -> {
                Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
                JOptionPane.showMessageDialog(this, 
                        "No puede marcar la entrada. Tiene otra en la misma fecha " + fecha, 
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
            case 5 -> {
                Sonido.ReproducirSonido("/sound/Minecraft-XP-Sound.wav");
                JOptionPane.showMessageDialog(this, 
                        "Se realizó el registro de entrada exitosamente del empleado ID " + idEmpleado +
                                " en la fecha " + fecha + " con la hora de entrada " + hora, 
                        "Éxito", JOptionPane.INFORMATION_MESSAGE);
            }
            default -> {
                Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
                JOptionPane.showMessageDialog(null, 
                        "Error desconocido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
