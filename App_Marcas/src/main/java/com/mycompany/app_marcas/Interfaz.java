     public boolean validacionesInterfaz(String idEmpleado, String fecha, String hora) {
        if (!Validaciones.validarIdEmpleado(idEmpleado)){
            Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
            JOptionPane.showMessageDialog(this, "Ingrese un id existente (debe ser un número entero)", 
                                    "Error id", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!Validaciones.validarFecha(fecha)){
            Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
            JOptionPane.showMessageDialog(this, "Ingrese una fecha en el formato YYYY-MM-DD", 
                                    "Error fecha", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!Validaciones.validarHora(hora)){
            Sonido.ReproducirSonido("/sound/GTA-V-Wasted.wav");
            JOptionPane.showMessageDialog(this, "Ingrese una hora en el formato hh:mm:ss", 
                                    "Error hora", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
     
    private void jButton_entradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_entradaActionPerformed
        String idEmpleado = jTextField_codigo.getText();
        String fecha = jTextField_fecha.getText();
        String hora = jTextField_hora.getText();
        String baseDatos = "planta" + jComboBox_plantaBD.getSelectedItem().toString().toLowerCase();
        
        if (validacionesInterfaz(idEmpleado, fecha, hora))
            try {
                int solucion = SP_empleadoMarcarEntrada.Insertar_Entrada(idEmpleado, fecha, hora, baseDatos);
                mostrarMensajeEntrada(solucion, idEmpleado, fecha, hora);
             } catch (ParseException ex) {
                 Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
             }
    }//GEN-LAST:event_jButton_entradaActionPerformed

