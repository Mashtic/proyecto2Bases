    private void opcionGirarPagos(String IdPlanilla){
    try {
        Connection connection = SQLconnection.getConnection();
        String selectsql = "EXEC PagarPlanilla ?";
        try (PreparedStatement stmt = connection.prepareStatement(selectsql)) {
            stmt.setString(1, IdPlanilla);

            try (ResultSet rs = stmt.executeQuery()) {
                if (rs.next()) {
                    String resultado = rs.getString(1);
                    lblValidarPago.setText(resultado);
                    if ("La planilla ha sido pagada.".equals(resultado)){
                        btnImprimir.setVisible(true);
                    }
                }
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        } finally {
            connection.close();
        }
    } catch(Exception e){
            System.out.println(e);
        }
    }
    
    private String opcionImprimirComprobante(String IdPlanilla) {
    String str = "";
    try {
        Connection connection = SQLconnection.getConnection();
        String selectsql = "SELECT * FROM historicoPlanillas WHERE id = ?";
        try (PreparedStatement stmt = connection.prepareStatement(selectsql)) {
            stmt.setString(1, IdPlanilla);

            try (ResultSet rs = stmt.executeQuery()) {
                while (rs.next()) {
                    // Ajusta el formato seg√∫n las columnas de tu tabla
                    str += "ID: " + rs.getString("id") + "\n" +
                           "ID Empleado: " + rs.getString("idEmpleado") + "\n" +
                           "Fecha Inicio: " + rs.getString("fechaInicio") + "\n" +
                           "Fecha Final: " + rs.getString("fechaFinal") + "\n" +
                           "Salario Bruto: " + rs.getString("salarioBruto") + "\n" +
                           "Obligaciones: " + rs.getString("obligaciones") + "\n" +
                           "Salario Neto: " + rs.getString("salarioNeto") + "\n" +
                           "Pagada: " + rs.getString("pagada") + "\n";
                }
            }
        }
    } catch (SQLException ex) {
        System.out.println(ex.toString());
    }
    return str;
}
    

